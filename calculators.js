function add(a, b) {
	return a + b;
}
function sub(a, b) {
	return a - b;
}
function div(a, b) {
	return a / b;
}
function multi(a, b) {
	return a * b;
}
function rac(a) {
	return Math.sqrt(a);
}
function power(a) {
	return a * a;
}
function percentage(a, b) {
	return (a / 100) * b;
}
export { add, sub, div, multi, rac, power, percentage };
